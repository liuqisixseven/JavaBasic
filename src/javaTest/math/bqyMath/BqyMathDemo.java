package javaTest.math.bqyMath;

public class BqyMathDemo {
    public static void main(String[] args) {
        System.out.println("求平方根："+Math.sqrt(16));
        System.out.println("求两数的最大值："+Math.max(110, 30));
        System.out.println("求两数的最小值："+Math.min(15, 16));
        System.out.println("2的8次方："+Math.pow(2, 8));
        System.out.println("四舍五入："+Math.round(3.1415926));
    }
}
